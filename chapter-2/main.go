package main

import "fmt"

// Operators
// Strings
// Booleans
func main() {
	fmt.Println("1 + 1 =", 1+1)
	fmt.Println(len("Hello World"))
	fmt.Println("Hello World"[1])
	fmt.Println("Hello" + " World")
	fmt.Println(true && true)
	fmt.Println(true && false)
	fmt.Println(true || false)
	fmt.Println(true || true)
	fmt.Println(!true)
}
